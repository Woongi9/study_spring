@Slf4j
@Aspect
public class TraceAspect {

    @Before("@annotation(hello.aop.order.aop.exam.annotation.Trace)")
    public void doTrace(JoinPoint joinPoint) {
        Object[] args = joinPoint.getArgs();
        log.info("[trace] {} args={}", joinPoint.getSignature(), args);
    }
}
